movies = ["Ready Or Not", "Get Out", "Barbarian", "Lights Out", "Hush"]

# Using append to add at list end
movies.append("The Babysitter")
print(movies)

# Using insert to add at desired index
movies.insert(2, "The Ring")
print(movies)

# Removing a value
movies.remove("Get Out")
print(movies)

# Clearing list
movies.clear()
print(movies)