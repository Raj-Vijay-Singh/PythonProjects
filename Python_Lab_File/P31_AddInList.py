animals = ["Hyena", "Camel", "Anteater", "Boar", "Octopus"]

# Adding one element
animals[1:1] = ["Cheetah"]
print(animals)

# Adding multiple elements
animals[2:2] = ["Buffalo", "Shark", "Giraffe", "Tiger", "Zebra"]
print(animals)

# Adding at end
animals[12:12] = ["Falcon", "Sloth"]
print(animals)