import turtle
t = turtle.Turtle()
turtle.bgcolor("beige")
t.color("#138808")
t.speed(2)
t.pensize(5)
t.penup()
t.goto(-400,-250)
t.lt(90)
t.pendown()
start = t.pos()
for clr in ["#138808", "white", "#FF9933"]: #Green, White and Saffron
    t.penup()
    t.goto(start)
    t.pendown()
    t.setheading(90)
    t.color(clr)
    t.fillcolor(clr)
    t.begin_fill()
    t.penup()
    t.fd(5)
    t.pendown()
    t.fd(150)
    start = t.pos()
    t.rt(90)
    t.fd(800)
    t.rt(90)
    t.fd(150)
    t.rt(90)
    t.fd(800)
    t.end_fill()

t.penup()
t.bk(400)
t.lt(90)
t.fd(15)
t.rt(90)
t.pendown()
t.color("#000080") # Deep Navy
t.circle(130/2)
t.penup()
t.lt(90)
t.fd(65)
t.pendown()
t.pensize(1)
t.dot(20)

for i in range(24):
    t.fd(58)
    t.bk(58)
    t.rt(15)

t.rt(7.5)
for i in range(24):
    t.penup()
    t.fd(63)
    t.pendown()
    t.dot(10)
    t.penup()
    t.back(63)
    t.rt(15)

turtle.done()
